# include <stdio.h>
#include <math.h>
#include "chemistry.h"
#include "../data/data.h"

float wynik;

double distance(atoms[i], atoms[j])
{
    float dx = atom[i].x - atom[j].x;
    float dy = atom[i].x - atom[j].x;
    float dz = atom[i].x - atom[j].x;

    return wynik = sqrt(dx * dx + dy * dy + dz * dz);
}

